    private String IdCardNO;

    public IdcardInfoExtractor(String IdCardNO){
           if(IdCardNO.length() == 15 && IdCardNO.matches("^[1-9][0-9]{5}[0-9]{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)[0-9]{2}[0-9]")){
            this.IdCardNO = this.transIDCard15to18(IdCardNO);
           }else if(IdCardNO.length() == 18 && IdCardNO.matches("^[1-9][0-9]{5}(18|19|20)[0-9]{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)[0-9]{3}([0-9]|(X|x))")){
               this.IdCardNO = IdCardNO;
           }else{
               this.IdCardNO = "";
           }
    }

    //获取所在省
    public String getProvince(){
        if(StringUtils.isEmpty(this.IdCardNO)){
            return "";
        }else{
            return cityCodeMap.get(this.IdCardNO.substring(0,2));
        }

    }

    //获取所在市
